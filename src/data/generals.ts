export function getGeneralStats() {
  const stats = {
    total: generals.length,
    byFaction: {
      wei: generals.filter(g => g.faction === 'wei').length,
      shu: generals.filter(g => g.faction === 'shu').length,
      wu: generals.filter(g => g.faction === 'wu').length,
      neutral: generals.filter(g => g.faction === 'neutral').length,
    },
    byRarity: {
      legendary: generals.filter(g => g.rarity === 'legendary').length,
      rare: generals.filter(g => g.rarity === 'rare').length,
      common: generals.filter(g => g.rarity === 'common').length,
    },
  };
  return stats;
}
